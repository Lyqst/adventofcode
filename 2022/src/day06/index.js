import run from "aocrunner"

const part1 = (input) => {
  let i
  for (i = 0; i < input.length; i++) {
    if([...new Set(input.slice(i, i + 4).split(''))].length == 4) break
  }
  return i + 4
}

const part2 = (input) => {
  let i
  for (i = 0; i < input.length; i++) {
    if([...new Set(input.slice(i, i + 14).split(''))].length == 14) break
  }
  return i + 14
}

